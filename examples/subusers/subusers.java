      Request request = new Request();
      request.setMethod(Method.POST);
      request.setEndpoint("subusers");
      request.setBody("{\"username\":\"John@example.com\",\"ips\":[\"1.1.1.1\",\"2.2.2.2\"],\"password\":\"johns_password\",\"email\":\"John@example.com\"}");
